fun main(){

    // logical operators
    var andOperator = (6>1) && (5>2) // only be tru if both statement is true
    var orOperator = (1>5)||(6>3) // will be false if both statement is false
    var notOperator = !(5==5) //invert the result

    println(andOperator)
    println(orOperator)
    println(notOperator)
}