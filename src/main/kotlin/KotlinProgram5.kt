fun main (){
    var lessThan = 5 < 2 //false
    var greaterThan = 10>2 //true
    var greaterThanOrEqualTo = 10>=4 //true
    var lessThanOrEqualTo = 5<=2 //false
    var equalTo = 1==1 // true (equality operator )
    var notEqualTo = 1!=1 // false

    // output

    println(lessThan)
    println(greaterThan)
    println(greaterThanOrEqualTo)
    println(lessThanOrEqualTo)
    println(equalTo)
    println(notEqualTo)
}